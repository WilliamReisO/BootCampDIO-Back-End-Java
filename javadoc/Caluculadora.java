public class Caluculadora {

    /*
     * Este método é utilizado para somar ois numeros inteiros
     * 
     * @param numeroUm este é o primiiro parâmetro do método
     * 
     * @param numeroDois ete é o segundo parârametro do método
     * 
     * @return int o resultado deste método é a soma dos dois números.
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}
